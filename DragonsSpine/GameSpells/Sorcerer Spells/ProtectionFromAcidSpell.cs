using System;

namespace DragonsSpine.Spells
{
    [SpellAttribute(GameSpell.GameSpellID.Protection_from_Acid, "pracid", "Protection from Acid", "Target gains bonuses to saving throws, increased protection and additional armor class versus acid attacks.",
        Globals.eSpellType.Abjuration, Globals.eSpellTargetType.Single, 21, 14, 275000, "0231", true, true, true, true, false, Character.ClassType.Sorcerer)]
    public class ProtectionFromAcidSpell : ISpellHandler
    {
        public GameSpell ReferenceSpell
        {
            get;
            set;
        }

        public bool OnCast(Character caster, string args)
        {
            string[] sArgs = args.Split(" ".ToCharArray());

            Character target = ReferenceSpell.FindAndConfirmSpellTarget(caster, sArgs[sArgs.Length - 1]);

            if (target == null) { return false; }

            ReferenceSpell.SendGenericCastMessage(caster, target, true);

            ReferenceSpell.SendGenericEnchantMessage(caster, target);

            int skillLevel = Skills.GetSkillLevel(caster.magic);

            Effect.CreateCharacterEffect(Effect.EffectTypes.Protection_from_Acid, skillLevel * 5, target, skillLevel * 20, caster);
            return true;
        }
    }
}
